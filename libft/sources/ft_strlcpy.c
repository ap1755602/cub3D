#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	sl;

	i = 0;
	if (dst == NULL || src == NULL)
		return (0);
	sl = ft_strlen(src);
	if (dstsize == 0)
		return (sl);
	while (i < dstsize - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (sl);
}
